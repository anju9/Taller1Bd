  CREATE OR REPLACE TRIGGER "DBAUSER"."DECREASESTHENUMBEROFUNITS" 
after insert or update on 
factura_detalle 
for each row
begin
    update opciones
    set unidades_disponibles=unidades_disponibles-:new.cantidad
    where id_opciones=:new.opcion_id;
end;
