--  DDL for View ACCESORIOS
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "DBAUSER"."ACCESORIOS" ("ID_FACTURA", "ID_VENDEDOR", "NOMBRE", "ID_CLIENTE", "Nombre cliente", "ID_VEHICULO", "MARCA", "FABRICANTE", "ID_OPCIONES", "DESCRIPCION") AS 
  select f.id_factura,ve.id_vendedor,ve.nombre,c.id_cliente,c.nombre as "Nombre cliente",v.id_vehiculo,v.marca,v.fabricante,op.id_opciones,op.descripcion
 from facturas f 
 inner join vehiculos v on f.vehiculo_id = v.id_vehiculo 
 inner join clientes c on f.cliente_id = c.id_cliente 
 inner join opciones op  on v.opcion_id = op.id_opciones 
 inner join vendedores ve on f.vendedor_id = ve.id_vendedor
;
--------------------------------------------------------
--  DDL for View ORDERTODISPLAY
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "DBAUSER"."ORDERTODISPLAY" ("ID_OPCIONES", "MARCA", "CODIGO_OPCION", "FABRICANTE", "UNIDADES_DISPONIBLES") AS 
  select o.id_opciones,v.marca,o.codigo_opcion,v.fabricante,o.Unidades_Disponibles
 from vehiculos v 
 inner join opciones o on v.opcion_id=o.id_opciones
 where unidades_disponibles<=5
 order by o.id_opciones
;
